x = 6
y = 2
z = 7

print(f"{x} mas {y} es igual a {x+y} ")
print(f"{x} menos {y} es igual a {x-y} ")
print(f"{x} por {y} es igual a {x*y} ")
print(f"{x} dividio {y} es igual a {x/y} ")
#Division al piso, es sacar el numero que esta despues de la ,
print(f"{z} dividio al piso {y} es igual a {z//y}")
#Division por modulo, nos mostrara el numero que sobra de la division(Resto)
print(f"{z} modulo de {y} es igual a {z%y}")
#Potencia
print(f"{x} elevado a la  {y} es igual a {x**y} ")
print(f"{x} elevado a la  {3} es igual a {x**3} ")
#Raiz Cuadrada
print(f"Raiz cuadrada de {x} es  {x**0.5} ")